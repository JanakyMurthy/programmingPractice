using namespace std;


int main(){
    int n;
    cin >> n;
    vector<int> c(n);
    for(int c_i = 0;c_i < n;c_i++){
       cin >> c[c_i];
    }
    int cur = 0;
    int s = 0;
    while(cur != n-1)
    {
        if(cur == n-2) cur++;
        else if(c[cur+2]==0) cur+=2;
        else cur++;
        s++;
    }
    cout << s;
    return 0;
}
